package Model;

public class Car extends Automobile{

    public Car(int p,String n)
    {
        super(p,n);
    }

    @Override
    public int get_price() {
        return super.get_price();
    }

    @Override
    public String get_name() {
        return super.get_name();
    }

    @Override
    public String toString() {
        return super.toString();
    }
}
